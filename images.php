                meteorite hitting the planet. In this scenario it is like that 
                the majority of life as we know it would end. Without preparation 
                it is guaranteed all humans would be extinguished however modern 
                satellite systems would likely detect a meteor giving ample time
                for preparation. The image represents one of the various 
                possibilities that an apocalypse could occur however methods 
                that are not instantaneous such as global warming or the spread 
                of radiation could also result in an apocalypse.</p>

            <p>The news subpages contain a banner with a volcano and a red 
                background. This represents the apocalyptic event of a chain
                of volcanic eruptions which will turn the skies red.
                While this is one of the least likely events, volcanic 
                eruptions did change the global landscape of earth in the past.
                Red is symbolic in this image because it foreshadows something
                dangerous and deadly. Smoke comes out of the volcano to 
                indicate imminent destruction.</p>

            <p>In many apocalyptic portrays of media, the apocalypse or a
                world changing event is preceded by deadly and powerful 
